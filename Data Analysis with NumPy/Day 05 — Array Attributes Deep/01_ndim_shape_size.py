import numpy as np

# =========================
# 1D Array তৈরি
# =========================
arr1 = np.array([1, 2, 3, 4, 5])

print("Array:", arr1)

# ndim → কত dimension (1D, 2D, 3D)
print("Dimension (ndim):", arr1.ndim)

# shape → structure (কত element আছে প্রতিটি axis এ)
print("Shape:", arr1.shape)

# size → total element সংখ্যা
print("Size:", arr1.size)