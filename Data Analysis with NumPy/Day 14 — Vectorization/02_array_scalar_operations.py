import numpy as np

arr = np.array([1, 2, 3, 4, 5])

# -----------------------------------
# Scalar operation (single value)
# -----------------------------------

# প্রতিটি element এর সাথে 5 multiply
print(arr * 5)
# [5 10 15 20 25]

# প্রতিটি element এ 2 যোগ
print(arr + 2)
# [3 4 5 6 7]

# প্রতিটি element power
print(arr ** 2)
# [1 4 9 16 25]