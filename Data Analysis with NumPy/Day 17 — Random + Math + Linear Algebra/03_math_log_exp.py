import numpy as np

arr = np.array([1, 10, 100, 1000])

# -----------------------------------
# Log functions
# -----------------------------------

print(np.log(arr))     # natural log (ln)
print(np.log10(arr))   # base 10 log

# -----------------------------------
# Exponential
# -----------------------------------

print(np.exp([1, 2, 3]))
# e^1, e^2, e^3